				Function.CurrentComponent = "PJPCRLNK";
								Function.WaitLoadingFinished(Function.IsCurrentComponentModal(false));

				this.ScriptLogger.WriteLine("[PJPCRLNK] Performing Close on MainForm...", Logger.MessageType.INF);
